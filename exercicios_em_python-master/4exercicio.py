#entradas

metros = int(input("Informe o valor em metros: "))

#processamento

centimetros = (metros * 100)

#saída
print("{0} metros em centímetros é {1}".format(metros, centimetros)) #{0} recebe a primeira variável e {1} recebe a segunda variável, em {1} o 1 aparece dentro das chaves pois as variáveis são contadas iniciando em 0.
